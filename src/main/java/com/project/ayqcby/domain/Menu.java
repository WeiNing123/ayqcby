package com.project.ayqcby.domain;

public class Menu {
	private String yhqx;
	private String id;
	private String mc;
	private String pid;
	private String path;
	private String icon;
	private String css;
	private String state;
	private int px;

	public String getYhqx() {
		return yhqx;
	}

	public void setYhqx(String yhqx) {
		this.yhqx = yhqx;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMc() {
		return mc;
	}

	public void setMc(String mc) {
		this.mc = mc;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getCss() {
		return css;
	}

	public void setCss(String css) {
		this.css = css;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getPx() {
		return px;
	}

	public void setPx(int px) {
		this.px = px;
	}
}